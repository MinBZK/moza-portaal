function SidebarMenuItem({
  children,
  disabled = false,
  isNew = false,
  currentPage,
  text,
  route,
  notification = 0,
}: {
  children: ReactNode;
  disabled?: boolean;
  isNew?: boolean;
  currentPage: string | null;
  text: string;
  route: string;
  notification?: number;
}) {
  const isActive = currentPage?.split("/")[1] === route.split("/")[1];
  return (
    <li className={`mb-1.5 ${disabled ? "opacity-50" : ""}`}>
      <Link
        aria-disabled={disabled}
        href={route}
        className={`flex cursor-pointer items-center rounded-md px-4 py-1.5 break-words hover:bg-neutral-100 lg:w-3/4 ${isActive ? "text-primary font-bold hover:bg-sky-400/20 md:bg-sky-400/20" : ""} `}
      >
        <svg
          xmlns="http://www.w3.org/2000/svg"
          viewBox="0 0 22 22"
          className={`mt-1 mr-4 h-5 w-5 shrink-0 ${isActive ? "text-primary" : "text-black"}`}
        >
          {children}
        </svg>
        {text}

        {isNew && (
          <p className="ml-2 inline-block rounded-full bg-gradient-to-r from-yellow-100 via-yellow-200 to-yellow-300 px-3 text-sm font-bold tracking-wide text-yellow-600 shadow-md">
            Nieuw
          </p>
        )}
        {notification > 0 && (
          <span className="ml-auto inline-flex h-4 w-4 items-center justify-center rounded-full bg-red-600 text-xs font-bold text-white">
            {notification}
          </span>
        )}
      </Link>
    </li>
  );
}

